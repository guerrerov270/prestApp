package co.prestapp.VO;

public class MovimientoVO {

	private int idMovimiento;
	private String codigoMovimiento;
	private String fechaMovimiento;
	private String entradaMovimiento;
	private String salidaMovimiento;
	private String saldoMovimiento;

	public int getIdMovimiento() {
		return idMovimiento;
	}

	public void setIdMovimiento(int idMovimiento) {
		this.idMovimiento = idMovimiento;
	}

	public String getCodigoMovimiento() {
		return codigoMovimiento;
	}

	public void setCodigoMovimiento(String codigoMovimiento) {
		this.codigoMovimiento = codigoMovimiento;
	}

	public String getFechaMovimiento() {
		return fechaMovimiento;
	}

	public void setFechaMovimiento(String fechaMovimiento) {
		this.fechaMovimiento = fechaMovimiento;
	}

	public String getEntradaMovimiento() {
		return entradaMovimiento;
	}

	public void setEntradaMovimiento(String entradaMovimiento) {
		this.entradaMovimiento = entradaMovimiento;
	}

	public String getSalidaMovimiento() {
		return salidaMovimiento;
	}

	public void setSalidaMovimiento(String salidaMovimiento) {
		this.salidaMovimiento = salidaMovimiento;
	}

	public String getSaldoMovimiento() {
		return saldoMovimiento;
	}

	public void setSaldoMovimiento(String saldoMovimiento) {
		this.saldoMovimiento = saldoMovimiento;
	}

}
